//! `QTree`: the directory analog of [`QTerm`](crate::qterm::QTerm).
//!
//! Where a `QTerm` represents a single generated *file*, a `QTree` represents a
//! whole generated *directory tree*. Its leaves are ordinary `QTerm`s (generated
//! source, serialized later via [`STerm::coparse`](crate::term::STerm::coparse))
//! or raw byte blobs (assets copied verbatim). The host program builds a `QTree`
//! the same way it builds a `QTerm` — with host-language control flow, glyph
//! quotes, lifts, and emits — and a later materialization step (the sinks in
//! `sink.rs`) walks the tree and writes it out.
//!
//! This module is **always compiled** and has **no tree-sitter dependency**, so
//! wasm / no-parse consumers get it too — the same posture as
//! [`lift`](crate::lift). The tree is a plain runtime value: no grammar, no
//! expander, and no new surface syntax are involved (see
//! `docs/design/directory-scaffolding.md` §6.1–6.2, §6.8).
//!
//! ```
//! use quilt::prelude::*;
//!
//! let main_rs = leaf("source_file", "fn main() {}");
//! let t = tree! {
//!     "Cargo.toml" => raw(b"[package]\n".to_vec()),
//!     "src"        => dir! { "main.rs" => file(main_rs) },
//!     ".gitignore" => raw(b"/target\n".to_vec()),
//!     "tests"      => dir! {},   // empty dir kept
//! };
//! assert_eq!(t.len(), 4);
//! ```

use crate::qterm::QTerm;
use miette::{miette, IntoDiagnostic, Result};
use serde::{Deserialize, Serialize};
use std::path::{Path, PathBuf};
use std::sync::Arc;

/**************************************************************/

/// A single, validated path component (no `/`): non-empty, not `.`/`..`, no
/// `/`, `\`, or NUL, and never absolute.
///
/// Path templating happens *before* construction — the host computes the string
/// (`format!("{module}.rs")`) and hands a finished segment in. The tree never
/// interprets `/` itself; nested directories are nested [`QTree`]s. The
/// convenience builders ([`QTree::emit`], [`tree!`]) split a `"src/main.rs"`
/// string into segments at insert time, validating each component.
///
/// Serializes as a plain string; **deserialization re-validates** through
/// [`Segment::new`], so a tree read off the wire (`quilt scaffold`'s
/// [`emit_tree`]) can never smuggle in a `/`, `..`, or NUL segment.
#[derive(Debug, Clone, PartialEq, Eq, Hash, PartialOrd, Ord, Serialize, Deserialize)]
#[serde(try_from = "String", into = "String")]
pub struct Segment(String);

impl TryFrom<String> for Segment {
    type Error = String;
    fn try_from(s: String) -> std::result::Result<Self, String> {
        Self::new(s).map_err(|e| e.to_string())
    }
}

impl From<Segment> for String {
    fn from(seg: Segment) -> String {
        seg.0
    }
}

impl Segment {
    /// Validate `s` as a single path component. See the type docs for the rules.
    pub fn new(s: impl Into<String>) -> Result<Self> {
        let s = s.into();
        if s.is_empty() {
            return Err(miette!("path segment must not be empty"));
        }
        if s == "." || s == ".." {
            return Err(miette!("path segment must not be {s:?}"));
        }
        if s.contains('/') {
            return Err(miette!("path segment must not contain '/': {s:?}"));
        }
        // Reject the Windows separator too: defense-in-depth so a segment can
        // never be read as a path on any platform.
        if s.contains('\\') {
            return Err(miette!("path segment must not contain '\\': {s:?}"));
        }
        if s.contains('\0') {
            return Err(miette!("path segment must not contain NUL: {s:?}"));
        }
        Ok(Self(s))
    }

    #[must_use]
    pub fn as_str(&self) -> &str {
        &self.0
    }
}

impl std::fmt::Display for Segment {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_str(&self.0)
    }
}

/// Split a `/`-joined path into validated [`Segment`]s. Empty components (from a
/// leading, trailing, or doubled `/`) are an error, as are any segments that
/// fail [`Segment::new`].
fn split_segments(path: &str) -> Result<Vec<Segment>> {
    if path.is_empty() {
        return Err(miette!("empty path"));
    }
    path.split('/').map(Segment::new).collect()
}

