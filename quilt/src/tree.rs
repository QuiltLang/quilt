/// A directory tree whose leaves are [`Node`]s. Children are kept in insertion
/// order (a `Vec`, not a map) so output and manifests are deterministic; sort
/// only when a canonical form is wanted.
#[derive(Debug, Clone, Default, PartialEq, Serialize, Deserialize)]
pub struct QTree {
    entries: Vec<(Segment, Node)>,
}

impl QTree {
    #[must_use]
    pub fn new() -> Self {
        Self::default()
    }

    /// Number of direct children.
    #[must_use]
    pub fn len(&self) -> usize {
        self.entries.len()
    }

    #[must_use]
    pub fn is_empty(&self) -> bool {
        self.entries.is_empty()
    }

    /// The direct children, in insertion order.
    #[must_use]
    pub fn entries(&self) -> &[(Segment, Node)] {
        &self.entries
    }

    /// Look up a direct child by segment.
    #[must_use]
    pub fn get(&self, seg: &Segment) -> Option<&Node> {
        self.entries.iter().find(|(s, _)| s == seg).map(|(_, n)| n)
    }

    /// Insert (or replace) a leaf at `path`, creating intermediate directories.
    ///
    /// `path` is split on `/` into validated [`Segment`]s. Intermediate
    /// components are created as (or reused from) directories; it is an error if
    /// an intermediate component already exists as a non-directory. The final
    /// component is set, **replacing** any existing entry there (right wins) —
    /// use [`QTree::overlay`]/[`QTree::merge`] for structural composition.
    ///
    /// This is the directory analog of `←` (emit a file per loop iteration).
    pub fn emit(&mut self, path: impl AsRef<str>, node: Node) -> Result<()> {
        let segs = split_segments(path.as_ref())?;
        self.emit_segs(&segs, node)
    }

    fn emit_segs(&mut self, segs: &[Segment], node: Node) -> Result<()> {
        match segs {
            [] => Err(miette!("empty path")),
            [last] => {
                self.set(last.clone(), node);
                Ok(())
            }
            [head, rest @ ..] => self.descend_dir(head)?.emit_segs(rest, node),
        }
    }

    /// Get a `&mut` to the child directory at `seg`, creating it if absent.
    /// Errors if `seg` exists as a non-directory.
    fn descend_dir(&mut self, seg: &Segment) -> Result<&mut Self> {
        let idx = if let Some(i) = self.entries.iter().position(|(s, _)| s == seg) {
            i
        } else {
            self.entries.push((seg.clone(), Node::Dir(Self::new())));
            self.entries.len() - 1
        };
        match &mut self.entries[idx].1 {
            Node::Dir(t) => Ok(t),
            _ => Err(miette!(
                "path component {:?} is a file, not a directory",
                seg.as_str()
            )),
        }
    }

    /// Set a direct child, replacing any existing entry at the same segment but
    /// preserving its position; otherwise append.
    fn set(&mut self, seg: Segment, node: Node) {
        if let Some(i) = self.entries.iter().position(|(s, _)| *s == seg) {
            self.entries[i].1 = node;
        } else {
            self.entries.push((seg, node));
        }
    }

    /// Overlay `other` onto `self`: for each entry in `other`, **right wins**.
    /// Two directories at the same segment are overlaid recursively; any other
    /// collision replaces the left node with the right one.
    #[must_use]
    pub fn overlay(mut self, other: Self) -> Self {
        for (seg, node) in other.entries {
            if let Some(i) = self.entries.iter().position(|(s, _)| *s == seg) {
                if let (Node::Dir(existing), Node::Dir(new)) = (&mut self.entries[i].1, &node) {
                    let merged = std::mem::take(existing).overlay(new.clone());
                    self.entries[i].1 = Node::Dir(merged);
                } else {
                    self.entries[i].1 = node;
                }
            } else {
                self.entries.push((seg, node));
            }
        }
        self
    }

    /// Merge `other` into `self`, **erroring on any collision** except two
    /// directories at the same segment (which merge recursively).
    pub fn merge(mut self, other: Self) -> Result<Self> {
        for (seg, node) in other.entries {
            if let Some(i) = self.entries.iter().position(|(s, _)| *s == seg) {
                let placeholder = Node::Dir(Self::new());
                let existing = std::mem::replace(&mut self.entries[i].1, placeholder);
                let merged = match (existing, node) {
                    (Node::Dir(a), Node::Dir(b)) => Node::Dir(a.merge(b)?),
                    _ => return Err(miette!("merge collision at {:?}", seg.as_str())),
                };
                self.entries[i].1 = merged;
            } else {
                self.entries.push((seg, node));
            }
        }
        Ok(self)
    }

    /// Place `sub` under `prefix` (a `/`-joined path) and overlay it onto
    /// `self`. `QTree::new().graft("packages/hello", sub)` yields
    /// `packages/hello/<sub…>`.
    pub fn graft(self, prefix: impl AsRef<str>, sub: Self) -> Result<Self> {
        let segs = split_segments(prefix.as_ref())?;
        let mut nested = sub;
        for seg in segs.into_iter().rev() {
            let mut t = Self::new();
            t.entries.push((seg, Node::Dir(nested)));
            nested = t;
        }
        Ok(self.overlay(nested))
    }

    /// Recursively drop empty subdirectories (after pruning their children).
    /// Leaves and non-empty directories are kept.
    #[must_use]
    pub fn prune_empty(mut self) -> Self {
        self.entries = self
            .entries
            .into_iter()
            .filter_map(|(seg, node)| match node {
                Node::Dir(t) => {
                    let t = t.prune_empty();
                    if t.is_empty() {
                        None
                    } else {
                        Some((seg, Node::Dir(t)))
                    }
                }
                other => Some((seg, other)),
            })
            .collect();
        self
    }

    /// Transform every leaf (non-directory node), passing its relative path.
    /// Directories are recursed into but not themselves passed to `f`.
    #[must_use]
    pub fn map(mut self, mut f: impl FnMut(&Path, Node) -> Node) -> Self {
        self.map_at(&mut PathBuf::new(), &mut f);
        self
    }

    fn map_at(&mut self, base: &mut PathBuf, f: &mut impl FnMut(&Path, Node) -> Node) {
        for (seg, node) in &mut self.entries {
            base.push(seg.as_str());
            if let Node::Dir(t) = node {
                t.map_at(base, f);
            } else {
                let taken = std::mem::replace(node, Node::Dir(Self::new()));
                *node = f(base, taken);
            }
            base.pop();
        }
    }

    /// Keep only the leaves for which `pred` returns true (by relative path).
    /// Directories are recursed into and retained (use [`QTree::prune_empty`] to
    /// drop ones left empty).
    #[must_use]
    pub fn filter(self, mut pred: impl FnMut(&Path, &Node) -> bool) -> Self {
        let mut base = PathBuf::new();
        self.filter_at(&mut base, &mut pred)
    }

    /// A human-readable listing of every path in the tree, in walk order, one
    /// per line and tagged with its node kind. Used by [`emit_tree`] when a
    /// scaffold program runs standalone (no `quilt scaffold` driving it), so
    /// `quilt run prog.tree.rs.quilt` still shows what *would* be built.
    #[must_use]
    pub fn listing(&self) -> String {
        let mut out = String::new();
        self.listing_at(&mut PathBuf::new(), &mut out);
        out
    }

    fn listing_at(&self, base: &mut PathBuf, out: &mut String) {
        use std::fmt::Write as _;
        for (seg, node) in &self.entries {
            base.push(seg.as_str());
            let p = base.to_string_lossy();
            match node {
                Node::Dir(t) => {
                    let _ = writeln!(out, "{p}/");
                    t.listing_at(base, out);
                }
                Node::File { .. } => {
                    let _ = writeln!(out, "{p} (file)");
                }
                Node::Raw { bytes, .. } => {
                    let _ = writeln!(out, "{p} (raw, {} bytes)", bytes.len());
                }
                Node::Link { target } => {
                    let _ = writeln!(out, "{p} -> {}", target.display());
                }
            }
            base.pop();
        }
    }

    fn filter_at(self, base: &mut PathBuf, pred: &mut impl FnMut(&Path, &Node) -> bool) -> Self {
        let entries = self
            .entries
            .into_iter()
            .filter_map(|(seg, node)| {
                base.push(seg.as_str());
                let kept = match node {
                    Node::Dir(t) => Some((seg, Node::Dir(t.filter_at(base, pred)))),
                    leaf if pred(base, &leaf) => Some((seg, leaf)),
                    _ => None,
                };
                base.pop();
                kept
            })
            .collect();
        Self { entries }
    }
}

/**************************************************************/
// `quilt scaffold` side channel (issue #95): a ground-first host program builds
// a `QTree` and `emit_tree`s it; the CLI runs the program and materializes the
// result. The wire format is postcard (the same one the expand cache uses); the
// tree is handed over via a sidecar file so it never collides with the program's
// own stdout/stderr.

/// Environment variable naming the sidecar file `quilt scaffold` reads the
/// emitted [`QTree`] back from. The CLI sets it to a temp path before running
/// the program; [`emit_tree`] writes the postcard-serialized tree there.
pub const TREE_OUT_ENV: &str = "QUILT_TREE_OUT";

/// Prefix for the per-parameter environment variables `quilt scaffold` exports
/// from its `--set name=value` / `--values` arguments, read back by
/// [`scaffold_param`]. A parameter `name` is passed as `QUILT_PARAM_<name>`.
pub const PARAM_ENV_PREFIX: &str = "QUILT_PARAM_";

/// Emit a [`QTree`] for `quilt scaffold` to materialize (issue #95). When the
/// scaffold CLI is driving the program it sets [`TREE_OUT_ENV`] to a sidecar
/// file, and this writes the postcard-serialized tree there. Run standalone (no
/// `quilt scaffold`), it prints a human-readable [`listing`](QTree::listing)
/// instead, so `quilt run prog.tree.rs.quilt` still shows what would be built.
pub fn emit_tree(tree: &QTree) -> Result<()> {
    if let Some(path) = std::env::var_os(TREE_OUT_ENV) {
        let bytes = postcard::to_stdvec(tree).into_diagnostic()?;
        std::fs::write(path, bytes).into_diagnostic()
    } else {
        // Standalone run: show what would be built instead of writing it.
        print!("{}", tree.listing());
        Ok(())
    }
}

/// Read a scaffold parameter passed by `quilt scaffold --set <name>=<value>`
/// (or `--values`). The CLI exposes each as `QUILT_PARAM_<name>` (see
/// [`PARAM_ENV_PREFIX`]); a scaffold program reads them to vary the tree it
/// builds. Returns `None` when the parameter was not supplied.
#[must_use]
pub fn scaffold_param(name: &str) -> Option<String> {
    std::env::var(format!("{PARAM_ENV_PREFIX}{name}")).ok()
}

/**************************************************************/

/// Build a [`QTree`] from `"segment-path" => Node` entries. Keys may contain
/// `/` (split into nested directories at build time); values are [`Node`]s
/// (`file(..)`, `raw(..)`, or a nested [`dir!`]). Later keys win on collision.
///
/// ```
/// use quilt::prelude::*;
/// let t = tree! {
///     "Cargo.toml" => raw(b"[package]\n".to_vec()),
///     "src"        => dir! { "main.rs" => raw(b"fn main(){}".to_vec()) },
/// };
/// assert_eq!(t.len(), 2);
/// ```
#[macro_export]
macro_rules! tree {
    ($($key:expr => $val:expr),* $(,)?) => {{
        let mut __t = $crate::tree::QTree::new();
        $( __t.emit($key, $val).expect("invalid tree path in tree!{}"); )*
        __t
    }};
}

/// Build a directory [`Node`] from `"segment" => Node` entries (the same syntax
/// as [`tree!`]). `dir! {}` is an empty directory.
#[macro_export]
macro_rules! dir {
    ($($key:expr => $val:expr),* $(,)?) => {
        $crate::tree::Node::Dir($crate::tree! { $($key => $val),* })
    };
}

