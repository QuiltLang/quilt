/// Whether to stamp the `DO NOT EDIT. GENERATED BY …` marker on a leaf, and how
/// the comment is introduced. The actual stamping happens at write time (the
/// write-policy layer, issue #93); the tree just records the intent.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum HeaderPolicy {
    /// Stamp the marker as a line comment introduced by this leader
    /// (e.g. `"//"` for Rust/C-likes, `"#"` for shell/Python/TOML).
    Stamp(String),
    /// Do not stamp a header (the default for raw assets the user owns).
    Skip,
}

/// Per-leaf metadata: unix permission bits and the header-stamp policy.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct FileMeta {
    /// Unix permission bits (e.g. `0o755`). `None` leaves the sink's default.
    pub mode: Option<u32>,
    /// Whether the write layer stamps the DO-NOT-EDIT marker on this leaf.
    pub header: HeaderPolicy,
}

impl FileMeta {
    /// Metadata for a generated source file: header stamped (`//` leader).
    #[must_use]
    pub fn source() -> Self {
        Self {
            mode: None,
            header: HeaderPolicy::Stamp("//".to_string()),
        }
    }

    /// Metadata for a verbatim asset: no header.
    #[must_use]
    pub fn asset() -> Self {
        Self {
            mode: None,
            header: HeaderPolicy::Skip,
        }
    }
}

impl Default for FileMeta {
    fn default() -> Self {
        Self::asset()
    }
}

/**************************************************************/

/// A node in a [`QTree`]: a subdirectory, a generated file, a raw blob, or a
/// symlink.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub enum Node {
    /// A subdirectory (possibly empty — empty dirs are kept).
    Dir(QTree),
    /// A generated source file, serialized later via `QTerm::coparse()`.
    File { content: Arc<QTerm>, meta: FileMeta },
    /// Verbatim bytes — assets, `LICENSE`, images, lockfiles.
    Raw { bytes: Vec<u8>, meta: FileMeta },
    /// A symlink whose target is a relative path *within* the tree (the sink
    /// rejects targets that escape — see issue #92).
    Link { target: PathBuf },
}

impl Node {
    /// Set unix permission bits on a `File`/`Raw` leaf. No-op for dirs/links.
    #[must_use]
    pub fn mode(mut self, mode: u32) -> Self {
        if let Self::File { meta, .. } | Self::Raw { meta, .. } = &mut self {
            meta.mode = Some(mode);
        }
        self
    }

    /// Override the header policy on a `File`/`Raw` leaf. No-op for dirs/links.
    #[must_use]
    pub fn header(mut self, policy: HeaderPolicy) -> Self {
        if let Self::File { meta, .. } | Self::Raw { meta, .. } = &mut self {
            meta.header = policy;
        }
        self
    }

    /// Convenience: stamp no header on this leaf.
    #[must_use]
    pub fn no_header(self) -> Self {
        self.header(HeaderPolicy::Skip)
    }
}

/// A generated source-file leaf (header stamped by default). Accepts a `QTerm`
/// or `Arc<QTerm>`.
pub fn file(content: impl Into<Arc<QTerm>>) -> Node {
    Node::File {
        content: content.into(),
        meta: FileMeta::source(),
    }
}

/// A verbatim byte blob leaf (no header). Accepts anything `Into<Vec<u8>>`
/// (`&[u8]`, `Vec<u8>`, `String`, …).
pub fn raw(bytes: impl Into<Vec<u8>>) -> Node {
    Node::Raw {
        bytes: bytes.into(),
        meta: FileMeta::asset(),
    }
}

/// A symlink leaf pointing at a relative path within the tree.
pub fn link(target: impl Into<PathBuf>) -> Node {
    Node::Link {
        target: target.into(),
    }
}

